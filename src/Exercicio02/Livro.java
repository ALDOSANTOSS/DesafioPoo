package Exercicio02;

public class Livro {
    String autor;
    String terma;
    int qtdPag;

    public Livro() {
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getTerma() {
        return terma;
    }

    public void setTerma(String terma) {
        this.terma = terma;
    }

    public int getQtdPag() {
        return qtdPag;
    }

    public void setQtdPag(int qtdPag) {
        this.qtdPag = qtdPag;
    }
}
