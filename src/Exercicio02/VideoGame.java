package Exercicio02;

public class VideoGame {
    String marca;
    String modelo;
    boolean Usando;

    public VideoGame() {
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public boolean isUsando() {
        return Usando;
    }

    public void setUsando(boolean usando) {
        Usando = usando;
    }
}
